#include <stdio.h>
int main()
{
    int num,a,b;
    printf("Enter your desair number of star: ");
    scanf("%d",&num);
    for(a=1;a<=num;a++){
        for(b=1;b<=a;b++){
            printf("*");
        }
        printf("\n");
    }
    for(a=num-1;a>=1;a--){
        for(b=1;b<=a;b++){
            printf("*");
        }
        printf("\n");
    }
    return 0;
}
